package com.javatutorial.Day2;

public class TypeCasting {
    public static void main(String[] args) {

        // Implicit Type Conversion

        int a = 23;
        float b = a;
        System.out.println(b);

        // Explicit Type Conversion

        float c = 23.33f;
        int d = (int)c;
        System.out.println(d);
    }
}
